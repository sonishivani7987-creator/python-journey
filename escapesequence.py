#\n	New line	
# \t	Tab (space)	
#\\	Backslash	
#\'	Single quote	
#\"	Double quote	
#\b	Backspace


print("Hello World!\nWelcome to Python Programming.")  # New line
print("Hello\tWorld!")  # Tab  
print("This is a backslash: \\")  # Backslash 
print('It\'s a beautiful day!')  # Single quote
print("She said, \"Hello!\"")  # Double quote
print("Hello\bWorld!")  # Backspace
